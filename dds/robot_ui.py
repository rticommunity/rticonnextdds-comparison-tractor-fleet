def main():
    parser = argparse.ArgumentParser(
        description="Robot Fleet UI – subscribes to DDS topics and displays live data"
    )
    parser.add_argument(
        "--domain", type=int, default=0,
        help="DDS Domain ID (must match robot nodes and charging stations). Default: 0",
    )
    parser.add_argument(
        "--ui-port", type=int, default=5000,
        help="HTTP port for the dashboard UI  (default: 5000)",
    )
    args = parser.parse_args()

