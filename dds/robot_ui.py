def create_app(store: FleetStateStore, client: DdsFleetClient) -> Flask:
    """Create the Flask app.

    *store* is the read-only data source for SSE snapshots.
    *client* is used for sending commands and video frames.
    """
    app = Flask(__name__)

    @app.route("/")
    def index():
        return render_template_string(DASHBOARD_HTML)

    @app.route("/stream")
    def stream():
        def generate():
            while client.running:
                data = json.dumps(store.snapshot())
                yield f"data: {data}\n\n"
                time.sleep(0.2)  # 5 Hz
        return Response(generate(), mimetype="text/event-stream",
                        headers={"Cache-Control": "no-cache", "X-Accel-Buffering": "no"})

    @app.route("/command", methods=["POST"])
    def command():
        body = request.get_json(force=True)
        robot_id = body.get("robot_id", "")
        cmd = body.get("command", "")
        goto_x = float(body.get("goto_x", 0))
        goto_y = float(body.get("goto_y", 0))
        goto_speed = float(body.get("goto_speed", 0))
        waypoints = body.get("waypoints", None)
        if robot_id == "*":
            result = client.send_command_all(cmd,
                                             goto_x=goto_x, goto_y=goto_y,
                                             goto_speed=goto_speed,
                                             waypoints=waypoints)
        else:
            result = client.send_command(robot_id, cmd,
                                         goto_x=goto_x, goto_y=goto_y,
                                         goto_speed=goto_speed,
                                         waypoints=waypoints)
        return jsonify(result)

    @app.route("/video_feed/<robot_id>")
    def video_feed(robot_id):
        """MJPEG stream from a robot's onboard camera via DDS VideoFrame topic.

        On first call (or when the robot changes), switches the video
        Subscriber's partition to the selected robot_id.  Only the
        matched robot's DataWriter publishes frames — all others see
        publication_matched_status.current_count == 0 and skip rendering.

        The browser renders this with a simple ``<img src="…">``.
        Each JPEG frame is delivered as a MIME multipart chunk.
        """
        # Tell DDS to match this robot's partition
        client.select_video_robot(robot_id)
        my_gen = client._video_generation

        def generate():
            _yield_count = 0
            _miss_count = 0
            while client.running:
                # Exit if user switched to another robot
                if client._video_generation != my_gen:
                    print(f"[ui] 📷 MJPEG {robot_id}: generation changed "
                          f"({my_gen} → {client._video_generation}) — exiting")
                    break
                frame = client.get_video_frame(robot_id)
                if frame:
                    _yield_count += 1
                    _miss_count = 0
                    if _yield_count % 25 == 0:
                        print(f"[ui] 📷 MJPEG {robot_id}: "
                              f"{_yield_count} yields")
                    yield (b"--frame\r\n"
                           b"Content-Type: image/jpeg\r\n\r\n"
                           + frame + b"\r\n")
                else:
                    _miss_count += 1
                    if _miss_count == 50:
                        active = client._active_video_robot
                        print(f"[ui] 📷 MJPEG {robot_id}: "
                              f"{_miss_count} consecutive misses "
                              f"(active_robot={active})")
                time.sleep(0.2)
            print(f"[ui] 📷 MJPEG {robot_id}: generator exited "
                  f"({_yield_count} yields total)")

        return Response(generate(),
                        mimetype="multipart/x-mixed-replace; boundary=frame",
                        headers={"Cache-Control": "no-cache"})

    @app.route("/video_stop", methods=["POST"])
    def video_stop():
        """Stop the video stream — reset the Subscriber partition.

        Without a matching partition, all robots' writers see
        publication_matched_status.current_count == 0 and stop
        rendering.  Zero CPU, zero network.
        """
        client.deselect_video()
        return jsonify({"success": True})

    @app.route("/clear_coverage", methods=["POST"])
    def clear_coverage():
        """Clear accumulated coverage data in the store."""
        store.clear_coverage()
        return jsonify({"success": True})

    @app.route("/ground_texture.jpg")
    def ground_texture():
        """Serve the farmland aerial-photo used as the 2D canvas background."""
        return send_from_directory(
            os.path.join(os.path.dirname(os.path.abspath(__file__)), '..', 'shared'),
            "field1_map.jpg",
            mimetype="image/jpeg",
        )

    return app


