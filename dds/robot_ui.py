"""
Robot Fleet UI – the DDS approach: Pure DDS (Connext)

A live web dashboard that subscribes to a fleet of robot nodes and
charging stations via DDS, displays real-time Kinematic State, Operational
State, Intent, Telemetry, and Charging Station status, and lets the
operator send commands to individual robots.

Architecture:
    FleetStateStore  – transport-agnostic data store (same concept as the gRPC approach)
    DdsFleetClient   – DDS communication layer; 6 DataReaders + 1 Requester
    Flask / HTML / JS – presentation layer; reads store via SSE, sends commands
                        through the client

Key differences from the gRPC approach:
  • No gRPC, no Zeroconf, no per-robot connections, no reconnect loops.
  • 6 DataReaders total (one per topic type) — flat regardless of fleet size.
  • 1 rti.rpc.Requester for commands — fan-out is automatic.
  • DDS SPDP replaces mDNS discovery — zero CLI flags needed.
  • ~4 threads total vs ~22 in the gRPC approach for a 3-robot fleet.

Usage:
    python robot_ui.py [--domain 0] --ui-port 5000
"""

