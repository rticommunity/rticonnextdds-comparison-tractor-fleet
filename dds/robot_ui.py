# ─────────────────────────────────────────────────────────────────────────────
# Fleet State Store – transport-agnostic, thread-safe data store
# ─────────────────────────────────────────────────────────────────────────────
class FleetStateStore:
    """Thread-safe store for fleet state, intent, telemetry, and charging info.

    This class has **no knowledge** of DDS, protobuf, or any transport.
    It holds plain Python dicts and provides a snapshot for the UI layer.
    """

    def __init__(self, domain_id: int = 0):
        self.lock = threading.Lock()
        self.kinematic: Dict[str, dict] = {}      # robot_id → {x, y, z, vx, vy, vz, heading, ts}
        self.operational: Dict[str, dict] = {}     # robot_id → {status, battery, ts}
        self.intent: Dict[str, dict] = {}          # robot_id → {type, target_x, target_y, ts, ...}
        self.telemetry: Dict[str, dict] = {}       # robot_id → {cpu, memory, temperature, signal_strength, ts}
        self.charging: Dict[str, dict] = {}        # station_id → {station_id, dock_robot, queue, dock_x, dock_y}
        self.coverage: Dict[str, list] = {}        # robot_id → [{x, y, seq}, …] (sorted by seq)
        self.domain_id = domain_id

    # ── reads ──────────────────────────────────────────────────────────────

    def snapshot(self) -> dict:
        """Return a JSON-serialisable copy of all data stores."""
        with self.lock:
            # Build merged state for the map (position + status + velocity)
            merged: Dict[str, dict] = {}
            for rid, kd in self.kinematic.items():
                merged[rid] = dict(kd)
            for rid, od in self.operational.items():
                if rid in merged:
                    merged[rid].update(od)
                else:
                    merged[rid] = dict(od)
            return {
                "state": merged,
                "kinematic": {k: dict(v) for k, v in self.kinematic.items()},
                "operational": {k: dict(v) for k, v in self.operational.items()},
                "intent": dict(self.intent),
                "telemetry": dict(self.telemetry),
                "charging": {k: dict(v) for k, v in self.charging.items()},
                "coverage": {k: list(v) for k, v in self.coverage.items()},
                "domain_id": self.domain_id,
            }

    def known_robots(self) -> List[str]:
        """Return sorted list of robot_ids that have kinematic data."""
        with self.lock:
            return sorted(self.kinematic.keys())

    # ── writes (called by the DDS client) ─────────────────────────────────

    def update_kinematic(self, robot_id: str, data: dict):
        """Store a kinematic state update."""
        with self.lock:
            self.kinematic[robot_id] = data

    def update_operational(self, robot_id: str, data: dict):
        """Store an operational state update."""
        with self.lock:
            self.operational[robot_id] = data

    def update_intent(self, robot_id: str, data: dict):
        """Store an intent update."""
        with self.lock:
            self.intent[robot_id] = data

    def update_telemetry(self, robot_id: str, data: dict):
        """Store a telemetry update."""
        with self.lock:
            self.telemetry[robot_id] = data

    def update_charging(self, station_id: str, data: dict):
        """Store a charging station status update."""
        with self.lock:
            self.charging[station_id] = data

    def update_coverage(self, robot_id: str, x: float, y: float):
        """Append a coverage point (from CoveragePoint DDS topic)."""
        with self.lock:
            if robot_id not in self.coverage:
                self.coverage[robot_id] = []
            self.coverage[robot_id].append({"x": x, "y": y})

    def clear_coverage(self):
        """Clear all accumulated coverage data."""
        with self.lock:
            self.coverage.clear()

    def remove_robot(self, robot_id: str):
        """Remove all data for a robot (liveliness lost)."""
        with self.lock:
            self.kinematic.pop(robot_id, None)
            self.operational.pop(robot_id, None)
            self.intent.pop(robot_id, None)
            self.telemetry.pop(robot_id, None)


# ─────────────────────────────────────────────────────────────────────────────
# DDS Fleet Client – all DDS / transport logic lives here
# ─────────────────────────────────────────────────────────────────────────────
class DdsFleetClient:
    """Owns the DDS DomainParticipant and all DDS entities.

    Creates 6 DataReaders (one per topic type) that receive data from
    ALL robots and stations simultaneously — no per-robot connections.
    Uses rti.rpc.Requester for sending commands.
    """

    def __init__(self, store: FleetStateStore, domain_id: int = 0):
        self.store = store
        self.domain_id = domain_id
        self.running = True
        self.participant: Optional[dds.DomainParticipant] = None
        self.command_requester: Optional[rpc.Requester] = None
        # Video frame buffer: robot_id → latest JPEG bytes
        self._video_frames: Dict[str, bytes] = {}
        self._video_lock = threading.Lock()
        # ── Video reader (partition-based matching) ──
        self._video_reader: Optional[dds.DataReader] = None
        self._video_subscriber: Optional[dds.Subscriber] = None
        self._active_video_robot: Optional[str] = None
        self._video_generation: int = 0  # bumped on each select/deselect

    def stop(self):
        self.running = False

    # ── DDS initialisation ─────────────────────────────────────────────────

    def start(self):
        """Create the DDS participant, readers, listeners, and requester."""
        print(f"[ui] Initializing DDS on domain {self.domain_id}...")

        # Resolve robot_qos.xml relative to this script
        script_dir = os.path.dirname(os.path.abspath(__file__))
        qos_xml = os.path.join(script_dir, "robot_qos.xml")
        qos_provider = dds.QosProvider(qos_xml)

        self.participant = dds.DomainParticipant(self.domain_id)

        # ── Topics ────────────────────────────────────────────────────────
        kin_topic     = dds.Topic(self.participant, "KinematicState",   KinematicState)
        op_topic      = dds.Topic(self.participant, "OperationalState", OperationalState)
        intent_topic  = dds.Topic(self.participant, "Intent",           Intent)
        telem_topic   = dds.Topic(self.participant, "Telemetry",        Telemetry)
        video_topic   = dds.Topic(self.participant, "VideoFrame",       VideoFrame)
        station_topic = dds.Topic(self.participant, "StationStatus",    StationStatus)
        cov_topic     = dds.Topic(self.participant, "CoveragePoint",   CoveragePoint)

        subscriber = dds.Subscriber(self.participant)

        # ── DataReaders with QoS from profiles ────────────────────────────
        kin_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::KinematicStateProfile")
        op_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::OperationalStateProfile")
        intent_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::IntentProfile")
        telem_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::TelemetryProfile")
        video_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::VideoProfile")
        station_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::StationStatusProfile")
        cov_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::CoverageProfile")

        self._kin_reader     = dds.DataReader(subscriber, kin_topic,     kin_r_qos)
        self._op_reader      = dds.DataReader(subscriber, op_topic,      op_r_qos)
        self._intent_reader  = dds.DataReader(subscriber, intent_topic,  intent_r_qos)
        self._telem_reader   = dds.DataReader(subscriber, telem_topic,   telem_r_qos)
        self._station_reader = dds.DataReader(subscriber, station_topic, station_r_qos)
        self._cov_reader     = dds.DataReader(subscriber, cov_topic,     cov_r_qos)

        # ── Video reader: partition-based matching ─────────────────────
        # Each robot's VideoFrame DataWriter sits on a Publisher whose
        # partition is the robot's own ID (e.g. 'tractor_1').
        #
        # The UI creates a dedicated Subscriber for video whose partition
        # starts as '__NONE__' (matches nothing).  When the operator
        # selects a robot, we change the Subscriber partition to that
        # robot_id → DDS matches the writer/reader pair.  When the
        # operator stops, partition reverts to '__NONE__' → unmatch.
        #
        # Advantages over CFT + propagated PropertyQos:
        #   • No filter evaluation per sample — matching is at the
        #     partition level (most efficient DDS mechanism).
        #   • Partition changes trigger publication_matched_status on the
        #     writer side — fully event-driven, no polling needed.
        #   • Simpler: one QoS knob (partition) instead of two (CFT +
        #     propagated property).
        _VIDEO_SENTINEL = "__NONE__"
        video_sub_qos = dds.SubscriberQos()
        video_sub_qos.partition.name = [_VIDEO_SENTINEL]
        self._video_subscriber = dds.Subscriber(
            self.participant, video_sub_qos)
        self._video_reader = dds.DataReader(
            self._video_subscriber, video_topic, video_r_qos)

        # ── Polling thread for all readers ────────────────────────────────
        # We use a single polling thread instead of DataReaderListeners
        # because DDS listener callbacks hold an internal lock that can
        # complicate shutdown.  Polling at ~20 Hz is simpler and sufficient
        # for a 5 Hz SSE dashboard.
        threading.Thread(target=self._poll_readers, daemon=True).start()

        # ── Command Requester (rti.rpc) ───────────────────────────────────
        self.command_requester = rpc.Requester(
            request_type=CommandRequest,
            reply_type=CommandResponse,
            participant=self.participant,
            service_name="RobotCommand",
        )

        n_readers = 7
        print(f"[ui] DDS initialized — {n_readers} DataReaders + 1 Requester"
              f" (video via partition-based matching)")

    # ── Reader polling ─────────────────────────────────────────────────────

    def _poll_readers(self):
        """Poll all 7 DataReaders at ~20 Hz and push data into the store."""
        while self.running:
            try:
                self._poll_kinematic()
                self._poll_operational()
                self._poll_intent()
                self._poll_telemetry()
                self._poll_video()
                self._poll_station()
                self._poll_coverage()
            except Exception as e:
                print(f"[ui] poll error: {e}")
            time.sleep(0.05)  # 20 Hz

    def _poll_kinematic(self):
        for sample in self._kin_reader.take():
            if sample.info.valid:
                d = sample.data
                self.store.update_kinematic(d.robot_id, {
                    "x": d.position.x, "y": d.position.y, "z": d.position.z,
                    "vx": d.velocity.x, "vy": d.velocity.y, "vz": d.velocity.z,
                    "heading": d.heading,
                    "ts": time.time(),
                })

    def _poll_operational(self):
        for sample in self._op_reader.take():
            if sample.info.valid:
                d = sample.data
                self.store.update_operational(d.robot_id, {
                    "status": d.status.name,
                    "battery": d.battery_level,
                    "ts": time.time(),
                })

    def _poll_intent(self):
        for sample in self._intent_reader.take():
            if sample.info.valid:
                d = sample.data
                entry = {
                    "type": d.intent_type.name,
                    "target_x": d.target_position.x,
                    "target_y": d.target_position.y,
                    "ts": time.time(),
                }
                if d.path_waypoints:
                    entry["waypoints"] = [
                        {"x": wp.x, "y": wp.y} for wp in d.path_waypoints
                    ]
                    entry["waypoint_index"] = d.path_index
                self.store.update_intent(d.robot_id, entry)

    def _poll_telemetry(self):
        for sample in self._telem_reader.take():
            if sample.info.valid:
                d = sample.data
                self.store.update_telemetry(d.robot_id, {
                    "cpu": d.cpu_usage,
                    "memory": d.memory_usage,
                    "temperature": d.temperature,
                    "signal_strength": d.signal_strength,
                    "ts": time.time(),
                })

    def _poll_video(self):
        for sample in self._video_reader.take():
            if sample.info.valid:
                d = sample.data
                with self._video_lock:
                    self._video_frames[d.robot_id] = bytes(d.frame_data)
                # Periodic frame-arrival log (every 25th frame)
                key = f'_vid_rx_{d.robot_id}'
                n = getattr(self, key, 0) + 1
                setattr(self, key, n)
                if n % 25 == 0:
                    print(f"[ui] 📷 poll_video: {n} frames received from {d.robot_id}")

    def _poll_station(self):
        for sample in self._station_reader.take():
            if sample.info.valid:
                d = sample.data
                queue = [
                    {"robot_id": e.robot_id, "rank": e.rank, "confirmed": e.confirmed}
                    for e in d.queue
                ]
                self.store.update_charging(d.station_id, {
                    "station_id": d.station_id,
                    "dock_x": d.dock_x,
                    "dock_y": d.dock_y,
                    "dock_robot": d.docked_robot_id,
                    "queue": queue,
                })

    def _poll_coverage(self):
        for sample in self._cov_reader.take():
            if sample.info.valid:
                d = sample.data
                self.store.update_coverage(d.robot_id, d.x, d.y)

    # ── command sending ────────────────────────────────────────────────────

    def send_command(self, robot_id: str, command: str, *,
                     goto_x: float = 0, goto_y: float = 0,
                     goto_speed: float = 0,
                     waypoints: Optional[List[dict]] = None) -> dict:
        """Send a command to a robot via rti.rpc.Requester."""
        cmd_map = {
            "STOP":        Command.CMD_STOP,
            "FOLLOW_PATH": Command.CMD_FOLLOW_PATH,
            "GOTO":        Command.CMD_GOTO,
            "RESUME":      Command.CMD_RESUME,
            "SET_PATH":    Command.CMD_SET_PATH,
            "CHARGE":      Command.CMD_CHARGE,
        }
        cmd_enum = cmd_map.get(command)
        if cmd_enum is None:
            return {"success": False, "description": f"Unknown command: {command}"}

        # Build typed parameters (no JSON encoding)
        goto_params = None
        set_path_params = None
        if cmd_enum == Command.CMD_GOTO:
            goto_params = GotoParameters(x=goto_x, y=goto_y, speed=goto_speed)
        elif cmd_enum == Command.CMD_SET_PATH and waypoints:
            set_path_params = SetPathParameters(
                waypoints=[Waypoint(x=w["x"], y=w["y"]) for w in waypoints])
        elif cmd_enum in (Command.CMD_FOLLOW_PATH, Command.CMD_RESUME) and goto_speed > 0:
            goto_params = GotoParameters(speed=goto_speed)

        req = CommandRequest(
            robot_id=robot_id,
            command=cmd_enum,
            goto_params=goto_params,
            set_path_params=set_path_params,
        )

        try:
            request_id = self.command_requester.send_request(req)
            replies = self.command_requester.receive_replies(
                dds.Duration.from_seconds(5),
                related_request_id=request_id,
            )
            # Find the reply from the target robot (fan-out means all robots
            # respond, but only the target robot's reply is meaningful)
            for reply in replies:
                if reply.info.valid and reply.data.robot_id == robot_id:
                    r = reply.data
                    return {
                        "success": r.success,
                        "description": r.description,
                        "resulting_status": r.resulting_status.name,
                        "resulting_intent": r.resulting_intent.name,
                    }
            return {"success": False, "description": "No matching robot responded"}
        except dds.TimeoutError:
            return {"success": False, "description": f"Timeout waiting for {robot_id}"}
        except Exception as e:
            return {"success": False, "description": str(e)}

    def send_command_all(self, command: str, *,
                         goto_x: float = 0, goto_y: float = 0,
                         goto_speed: float = 0,
                         waypoints: Optional[List[dict]] = None) -> dict:
        """Send a command to every known robot, one at a time."""
        robot_ids = self.store.known_robots()
        results: dict = {}
        for rid in robot_ids:
            results[rid] = self.send_command(rid, command,
                                             goto_x=goto_x, goto_y=goto_y,
                                             goto_speed=goto_speed,
                                             waypoints=waypoints)
        n = len(robot_ids)
        successes = sum(1 for r in results.values() if r.get("success"))
        return {
            "broadcast": True,
            "command": command,
            "calls_made": n,
            "success": successes == n,
            "description": f"{successes}/{n} robots acknowledged {command}",
            "results": results,
        }

    def get_video_frame(self, robot_id: str) -> Optional[bytes]:
        with self._video_lock:
            return self._video_frames.get(robot_id)

    # ── Video robot selection (partition-based matching) ──────────

    _VIDEO_SENTINEL = "__NONE__"

    def select_video_robot(self, robot_id: str):
        """Switch the onboard camera to a different robot.

        A single DDS knob is turned — the video Subscriber's partition:

        **Partition QoS** on ``self._video_subscriber`` → ``[robot_id]``

        Each robot's VideoFrame DataWriter sits on a Publisher whose
        partition is the robot's own ID.  Changing the Subscriber
        partition to a robot_id makes DDS match that specific
        writer/reader pair, which:
          • triggers ``publication_matched_status`` on the matched
            robot's writer (current_count goes from 0 → 1) — the robot
            starts rendering and publishing JPEG frames.
          • unmatches the previous robot (current_count goes from
            1 → 0) — it stops rendering immediately.

        No content filter, no property inspection, no polling.
        """
        if robot_id == self._active_video_robot:
            return  # already viewing this robot

        # Clear stale frames from the previous robot
        with self._video_lock:
            self._video_frames.clear()

        # Bump generation — kills orphan MJPEG generators
        self._video_generation += 1

        # Change the video Subscriber's partition to the selected robot
        qos = self._video_subscriber.qos
        qos.partition.name = [robot_id]
        self._video_subscriber.qos = qos

        self._active_video_robot = robot_id
        print(f"[ui] 📷 Video switched to {robot_id} (gen={self._video_generation})")

    def deselect_video(self):
        """Stop the video stream — set Subscriber partition back to sentinel.

        All robot writers see ``publication_matched_status.current_count == 0``
        → none of them render.  The Subscriber partition ``'__NONE__'``
        doesn't match any Publisher partition → no data flows.
        """
        # Subscriber partition → sentinel (matches nothing)
        qos = self._video_subscriber.qos
        qos.partition.name = [self._VIDEO_SENTINEL]
        self._video_subscriber.qos = qos

        self._active_video_robot = None
        self._video_generation += 1  # kill orphan MJPEG generators
        with self._video_lock:
            self._video_frames.clear()
        print(f"[ui] 📷 Video stopped — all robots idle (gen={self._video_generation})")


