#!/usr/bin/env python3
"""
Robot Fleet UI – the hybrid approach: Hybrid DDS Pub-Sub + gRPC Request-Reply

A live web dashboard that subscribes to fleet data via DDS (Connext)
and sends operator commands via gRPC (resolved through Zeroconf mDNS).

Architecture:
    FleetStateStore  – transport-agnostic data store (same as the DDS approach)
    HybridFleetClient – DDS readers for pub-sub + gRPC stubs for commands
    Flask / HTML / JS  – presentation layer; same dashboard UI

Key design:
  • 6 DDS DataReaders (one per topic type) for receiving fleet data —
    identical to the DDS approach.  Auto-discovery via DDS SPDP.
  • Partition-based video matching — identical to the DDS approach.
  • gRPC unary RPC for operator commands — each robot runs a gRPC
    server discovered via Zeroconf mDNS.
  • No gRPC streaming — DDS handles all pub-sub natively.

Compare:
  the gRPC approach: Full mesh gRPC — N robots × 4 streaming RPCs per peer = 12+.
  the DDS approach: Pure DDS — rti.rpc Requester/Replier for commands.
  the hybrid approach: Hybrid — best of both: DDS pub-sub + gRPC request-reply.

Usage:
    python robot_ui.py [--domain 0] [--ui-port 5000] [--same-host]
"""

