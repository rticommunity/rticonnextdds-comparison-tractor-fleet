# ─────────────────────────────────────────────────────────────────────────────
# Fleet State Store – transport-agnostic, thread-safe data store
# ─────────────────────────────────────────────────────────────────────────────
class FleetStateStore:
    """Thread-safe store for fleet state, intent, telemetry, and charging info.

    This class has **no knowledge** of DDS, gRPC, or any transport.
    It holds plain Python dicts and provides a snapshot for the UI layer.

    Additionally stores gRPC addresses for robots discovered via Zeroconf
    so the command path can resolve them at send time.
    """

    def __init__(self, domain_id: int = 0):
        self.lock = threading.Lock()
        self.kinematic: Dict[str, dict] = {}
        self.operational: Dict[str, dict] = {}
        self.intent: Dict[str, dict] = {}
        self.telemetry: Dict[str, dict] = {}
        self.charging: Dict[str, dict] = {}
        self.coverage: Dict[str, list] = {}
        self.domain_id = domain_id
        # gRPC address book: robot_id → "host:port"
        self._grpc_addresses: Dict[str, str] = {}

    # ── reads ──────────────────────────────────────────────────────────────

    def snapshot(self) -> dict:
        with self.lock:
            merged: Dict[str, dict] = {}
            for rid, kd in self.kinematic.items():
                merged[rid] = dict(kd)
            for rid, od in self.operational.items():
                if rid in merged:
                    merged[rid].update(od)
                else:
                    merged[rid] = dict(od)
            return {
                "state": merged,
                "kinematic": {k: dict(v) for k, v in self.kinematic.items()},
                "operational": {k: dict(v) for k, v in self.operational.items()},
                "intent": dict(self.intent),
                "telemetry": dict(self.telemetry),
                "charging": {k: dict(v) for k, v in self.charging.items()},
                "coverage": {k: list(v) for k, v in self.coverage.items()},
                "domain_id": self.domain_id,
                "grpc_peers": len(self._grpc_addresses),
            }

    def known_robots(self) -> List[str]:
        with self.lock:
            return sorted(self.kinematic.keys())

    def resolve_grpc_address(self, robot_id: str) -> Optional[str]:
        """Look up the gRPC address for a robot discovered via Zeroconf."""
        with self.lock:
            return self._grpc_addresses.get(robot_id)

    # ── writes (called by the transport layer) ─────────────────────────────

    def register_grpc_address(self, robot_id: str, address: str):
        with self.lock:
            self._grpc_addresses[robot_id] = address

    def update_kinematic(self, robot_id: str, data: dict):
        with self.lock:
            self.kinematic[robot_id] = data

    def update_operational(self, robot_id: str, data: dict):
        with self.lock:
            self.operational[robot_id] = data

    def update_intent(self, robot_id: str, data: dict):
        with self.lock:
            self.intent[robot_id] = data

    def update_telemetry(self, robot_id: str, data: dict):
        with self.lock:
            self.telemetry[robot_id] = data

    def update_charging(self, station_id: str, data: dict):
        with self.lock:
            self.charging[station_id] = data

    def update_coverage(self, robot_id: str, points: list):
        with self.lock:
            self.coverage.setdefault(robot_id, []).extend(points)

    def clear_coverage(self):
        with self.lock:
            self.coverage.clear()

    def remove_robot(self, robot_id: str):
        with self.lock:
            self.kinematic.pop(robot_id, None)
            self.operational.pop(robot_id, None)
            self.intent.pop(robot_id, None)
            self.telemetry.pop(robot_id, None)


# ─────────────────────────────────────────────────────────────────────────────
# Hybrid Fleet Client – DDS readers + gRPC command stubs
# ─────────────────────────────────────────────────────────────────────────────
class HybridFleetClient:
    """DDS DataReaders for pub-sub data; gRPC stubs for commands.

    Creates 6 DataReaders (one per topic type) — identical to the DDS approach.
    Commands are sent via gRPC to robot addresses discovered by Zeroconf.
    """

    def __init__(self, store: FleetStateStore, domain_id: int = 0):
        self.store = store
        self.domain_id = domain_id
        self.running = True
        self.participant: Optional[dds.DomainParticipant] = None
        self._video_frames: Dict[str, bytes] = {}
        self._video_lock = threading.Lock()
        self._video_reader: Optional[dds.DataReader] = None
        self._video_subscriber: Optional[dds.Subscriber] = None
        self._active_video_robot: Optional[str] = None
        self._video_generation: int = 0  # bumped on each select/deselect

    def stop(self):
        self.running = False

    # ── DDS initialisation ─────────────────────────────────────────────────

    def start(self):
        """Create DDS participant, readers, and start polling thread."""
        print(f"[ui] Initializing DDS on domain {self.domain_id}...")

        script_dir = os.path.dirname(os.path.abspath(__file__))
        qos_xml = os.path.join(script_dir, "robot_qos.xml")
        qos_provider = dds.QosProvider(qos_xml)

        self.participant = dds.DomainParticipant(self.domain_id)

        # ── Topics ────────────────────────────────────────────────────────
        kin_topic     = dds.Topic(self.participant, "KinematicState",   KinematicState)
        op_topic      = dds.Topic(self.participant, "OperationalState", OperationalState)
        intent_topic  = dds.Topic(self.participant, "Intent",           Intent)
        telem_topic   = dds.Topic(self.participant, "Telemetry",        Telemetry)
        video_topic   = dds.Topic(self.participant, "VideoFrame",       VideoFrame)
        station_topic = dds.Topic(self.participant, "StationStatus",    StationStatus)

        subscriber = dds.Subscriber(self.participant)

        # ── DataReaders with QoS from profiles ────────────────────────────
        kin_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::KinematicStateProfile")
        op_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::OperationalStateProfile")
        intent_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::IntentProfile")
        telem_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::TelemetryProfile")
        video_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::VideoProfile")
        station_r_qos = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::StationStatusProfile")

        self._kin_reader     = dds.DataReader(subscriber, kin_topic,     kin_r_qos)
        self._op_reader      = dds.DataReader(subscriber, op_topic,      op_r_qos)
        self._intent_reader  = dds.DataReader(subscriber, intent_topic,  intent_r_qos)
        self._telem_reader   = dds.DataReader(subscriber, telem_topic,   telem_r_qos)
        self._station_reader = dds.DataReader(subscriber, station_topic, station_r_qos)

        # ── Coverage reader ───────────────────────────────────────────────
        cov_topic   = dds.Topic(self.participant, "CoveragePoint", CoveragePoint)
        cov_r_qos   = qos_provider.datareader_qos_from_profile(
            "RobotQosLibrary::CoverageProfile")
        self._cov_reader = dds.DataReader(subscriber, cov_topic, cov_r_qos)

        # ── Video reader: partition-based matching ─────────────────────
        _VIDEO_SENTINEL = "__NONE__"
        video_sub_qos = dds.SubscriberQos()
        video_sub_qos.partition.name = [_VIDEO_SENTINEL]
        self._video_subscriber = dds.Subscriber(
            self.participant, video_sub_qos)
        self._video_reader = dds.DataReader(
            self._video_subscriber, video_topic, video_r_qos)

        # ── Polling thread for all readers ────────────────────────────────
        threading.Thread(target=self._poll_readers, daemon=True).start()

        n_readers = 7
        print(f"[ui] DDS initialized — {n_readers} DataReaders"
              f" (video via partition-based matching)")
        print(f"[ui] Commands via gRPC (robot addresses resolved by Zeroconf)")

    # ── Reader polling ─────────────────────────────────────────────────────

    def _poll_readers(self):
        while self.running:
            try:
                self._poll_kinematic()
                self._poll_operational()
                self._poll_intent()
                self._poll_telemetry()
                self._poll_video()
                self._poll_station()
                self._poll_coverage()
            except Exception as e:
                print(f"[ui] poll error: {e}")
            time.sleep(0.05)  # 20 Hz

    def _poll_kinematic(self):
        for sample in self._kin_reader.take():
            if sample.info.valid:
                d = sample.data
                self.store.update_kinematic(d.robot_id, {
                    "x": d.position.x, "y": d.position.y, "z": d.position.z,
                    "vx": d.velocity.x, "vy": d.velocity.y, "vz": d.velocity.z,
                    "heading": d.heading,
                    "ts": time.time(),
                })

    def _poll_operational(self):
        for sample in self._op_reader.take():
            if sample.info.valid:
                d = sample.data
                self.store.update_operational(d.robot_id, {
                    "status": d.status.name,
                    "battery": d.battery_level,
                    "ts": time.time(),
                })

    def _poll_intent(self):
        for sample in self._intent_reader.take():
            if sample.info.valid:
                d = sample.data
                entry = {
                    "type": d.intent_type.name,
                    "target_x": d.target_position.x,
                    "target_y": d.target_position.y,
                    "ts": time.time(),
                }
                if d.path_waypoints:
                    entry["waypoints"] = [
                        {"x": wp.x, "y": wp.y} for wp in d.path_waypoints
                    ]
                    entry["waypoint_index"] = d.path_index
                self.store.update_intent(d.robot_id, entry)

    def _poll_telemetry(self):
        for sample in self._telem_reader.take():
            if sample.info.valid:
                d = sample.data
                self.store.update_telemetry(d.robot_id, {
                    "cpu": d.cpu_usage,
                    "memory": d.memory_usage,
                    "temperature": d.temperature,
                    "signal_strength": d.signal_strength,
                    "ts": time.time(),
                })

    def _poll_video(self):
        for sample in self._video_reader.take():
            if sample.info.valid:
                d = sample.data
                with self._video_lock:
                    self._video_frames[d.robot_id] = bytes(d.frame_data)

    def _poll_station(self):
        for sample in self._station_reader.take():
            if sample.info.valid:
                d = sample.data
                queue = [
                    {"robot_id": e.robot_id, "rank": e.rank, "confirmed": e.confirmed}
                    for e in d.queue
                ]
                self.store.update_charging(d.station_id, {
                    "station_id": d.station_id,
                    "dock_x": d.dock_x,
                    "dock_y": d.dock_y,
                    "dock_robot": d.docked_robot_id,
                    "queue": queue,
                })

    def _poll_coverage(self):
        for sample in self._cov_reader.take():
            if sample.info.valid:
                d = sample.data
                self.store.update_coverage(d.robot_id,
                    [{"x": d.x, "y": d.y}])

    # ── gRPC command sending ───────────────────────────────────────────────

    def send_command(self, robot_id: str, command: str, *,
                     goto_x: float = 0, goto_y: float = 0,
                     goto_speed: float = 0,
                     waypoints: Optional[List[dict]] = None) -> dict:
        """Send a command to a robot via gRPC (address resolved from store)."""
        address = self.store.resolve_grpc_address(robot_id)
        if not address:
            return {"success": False,
                    "description": f"No gRPC address for {robot_id} (not yet discovered via Zeroconf)"}

        # Map command string → protobuf enum
        cmd_map = {
            "STOP":        robot_pb2.CMD_STOP,
            "FOLLOW_PATH": robot_pb2.CMD_FOLLOW_PATH,
            "GOTO":        robot_pb2.CMD_GOTO,
            "RESUME":      robot_pb2.CMD_RESUME,
            "SET_PATH":    robot_pb2.CMD_SET_PATH,
            "CHARGE":      robot_pb2.CMD_CHARGE,
        }
        cmd_enum = cmd_map.get(command)
        if cmd_enum is None:
            return {"success": False, "description": f"Unknown command: {command}"}

        # Build protobuf request with typed parameters
        kwargs = dict(robot_id=robot_id, command=cmd_enum)
        if cmd_enum == robot_pb2.CMD_GOTO:
            kwargs["goto_params"] = robot_pb2.GotoParameters(
                x=goto_x, y=goto_y, speed=goto_speed,
            )
        elif cmd_enum in (robot_pb2.CMD_FOLLOW_PATH, robot_pb2.CMD_RESUME) and goto_speed > 0:
            kwargs["goto_params"] = robot_pb2.GotoParameters(speed=goto_speed)
        elif cmd_enum == robot_pb2.CMD_SET_PATH and waypoints:
            kwargs["set_path_params"] = robot_pb2.SetPathParameters(
                waypoints=[robot_pb2.Waypoint(x=w["x"], y=w["y"]) for w in waypoints],
            )
        req = robot_pb2.CommandRequest(**kwargs)

        try:
            channel = grpc.insecure_channel(address)
            grpc.channel_ready_future(channel).result(timeout=3)
            stub = robot_pb2_grpc.RobotCommandStub(channel)
            resp = stub.SendCommand(req, timeout=5)
            channel.close()
            return {
                "success": resp.success,
                "description": resp.description,
                "resulting_status": robot_pb2.RobotStatus.Name(resp.resulting_status),
                "resulting_intent": robot_pb2.RobotIntent.Name(resp.resulting_intent),
            }
        except Exception as e:
            return {"success": False, "description": str(e)}

    def send_command_all(self, command: str, *,
                         goto_x: float = 0, goto_y: float = 0,
                         goto_speed: float = 0,
                         waypoints: Optional[List[dict]] = None) -> dict:
        """Send a command to every known robot, one gRPC call at a time."""
        robot_ids = self.store.known_robots()
        results: dict = {}
        for rid in robot_ids:
            results[rid] = self.send_command(rid, command,
                                             goto_x=goto_x, goto_y=goto_y,
                                             goto_speed=goto_speed,
                                             waypoints=waypoints)
        n = len(robot_ids)
        successes = sum(1 for r in results.values() if r.get("success"))
        return {
            "broadcast": True,
            "command": command,
            "calls_made": n,
            "success": successes == n,
            "description": f"{successes}/{n} robots acknowledged {command}",
            "results": results,
        }

    # ── Video (DDS partition-based matching – same as the DDS approach) ───────────

    def get_video_frame(self, robot_id: str) -> Optional[bytes]:
        with self._video_lock:
            return self._video_frames.get(robot_id)

    _VIDEO_SENTINEL = "__NONE__"

    def select_video_robot(self, robot_id: str):
        if robot_id == self._active_video_robot:
            return
        with self._video_lock:
            self._video_frames.clear()
        self._video_generation += 1
        qos = self._video_subscriber.qos
        qos.partition.name = [robot_id]
        self._video_subscriber.qos = qos
        self._active_video_robot = robot_id
        print(f"[ui] Video switched to {robot_id} (gen={self._video_generation})")

    def deselect_video(self):
        qos = self._video_subscriber.qos
        qos.partition.name = [self._VIDEO_SENTINEL]
        self._video_subscriber.qos = qos
        self._active_video_robot = None
        self._video_generation += 1
        with self._video_lock:
            self._video_frames.clear()
        print(f"[ui] Video stopped — all robots idle (gen={self._video_generation})")


